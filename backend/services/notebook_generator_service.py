    @staticmethod
    def generate_python_script(dataset_id: str, mode: str = "clean", user_id=None) -> str:
        notebook = NotebookGeneratorService.generate_notebook_dict(dataset_id, mode=mode, user_id=user_id)
        script_parts = []
        for cell in notebook["cells"]:
            if cell["cell_type"] == "markdown":
                script_parts.append("# " + "="*60)
                for line in cell["source"]:
                    cleaned_line = line.rstrip("\n")
                    script_parts.append(f"# {cleaned_line}")
                script_parts.append("# " + "="*60 + "\n")
            elif cell["cell_type"] == "code":
                for line in cell["source"]:
                    script_parts.append(line.rstrip("\n"))
                script_parts.append("\n")
        return "\n".join(script_parts)

    @staticmethod
    def upload_to_gist(dataset_id: str, github_token: str, mode: str = "clean", user_id=None) -> str:
        import requests
        
        # 1. Generate notebook content
        notebook = NotebookGeneratorService.generate_notebook_dict(dataset_id, mode=mode, user_id=user_id)
        notebook_str = json.dumps(notebook, indent=2)
        
        # 2. Get dataset details
        dataset = DatasetService.get_dataset_by_id(dataset_id, user_id=user_id)
        filename = dataset.get("filename", "dataset.csv")
        gist_filename = filename.rsplit(".", 1)[0] + "_ADSA_Workflow.ipynb"
        
        # 3. Create Gist payload
        payload = {
            "description": f"ADSA Machine Learning Workflow Pipeline for {filename} ({mode.capitalize()} Mode)",
            "public": True,
            "files": {
                gist_filename: {
                    "content": notebook_str
                }
            }
        }
        
        headers = {
            "Authorization": f"token {github_token}",
            "Accept": "application/vnd.github.v3+json"
        }
        
        response = requests.post("https://api.github.com/gists", json=payload, headers=headers)
        if response.status_code != 201:
            raise Exception(f"GitHub API Error: {response.text}")
            
        gist_data = response.json()
        gist_id = gist_data.get("id")
        owner = gist_data.get("owner", {}).get("login", "anonymous")
        
        # 4. Return Colab Gist URL
        colab_url = f"https://colab.research.google.com/gist/{owner}/{gist_id}/{gist_filename}"
        return colab_url

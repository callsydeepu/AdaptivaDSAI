                    "    models = candidate_models",
                    "",
                    "for name, model in models.items():",
                    "    model.fit(train_features, train_target)",
                    "    predictions = model.predict(test_features)",
                    "    ",
                    "    acc = accuracy_score(test_target, predictions)",
                    "    prec = precision_score(test_target, predictions, average='weighted', zero_division=0)",
                    "    rec = recall_score(test_target, predictions, average='weighted', zero_division=0)",
                    "    f1 = f1_score(test_target, predictions, average='weighted', zero_division=0)",
                    "    cm = confusion_matrix(test_target, predictions)",
                    "    ",
                    "    results[name] = {",
                    "        'Accuracy': acc,",
                    "        'Precision': prec,",
                    "        'Recall': rec,",
                    "        'F1 Score': f1,",
                    "        'Confusion Matrix': cm",
